"""
Execution Module

Responsible for executing actions generated by the Planning Module and monitoring execution status.
"""

import sys
import os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), '../..'))

from typing import Optional, Dict, Any
from modules.execution.robot_interface import RobotInterface, RobotState
from modules.execution.mock_robot import MockRobot


class Executor:
    """
    Executor

    Responsibilities:
    1. Execute predefined actions
    2. Monitor execution status
    3. Collect feedback data
    """
    
    def __init__(self, robot: Optional[RobotInterface] = None):
        self.robot = robot or MockRobot()
    
    def execute_action(self, action_type: str, **params) -> Dict[str, Any]:
        """
        Execute a single action

        Args:
            action_type: Type of action (navigate/pick/place/search/speak)
            **params: Action parameters

        Returns:
            Feedback data: {success: bool, message: str, data: dict}
        """
        try:
            if action_type == "navigate":
                success = self.robot.navigate(params.get("target"))
                return {"success": success, "action": "navigate"}
            
            elif action_type == "search":
                result = self.robot.search(params.get("object_name"))
                return {"success": result is not None, "action": "search", "result": result}
            
            elif action_type == "pick":
                success = self.robot.pick(
                    params.get("object_name"),
                    params.get("object_id")
                )
                return {"success": success, "action": "pick"}
            
            elif action_type == "place":
                success = self.robot.place(params.get("location"))
                return {"success": success, "action": "place"}
            
            elif action_type == "speak":
                success = self.robot.speak(params.get("content"))
                return {"success": success, "action": "speak"}
            
            elif action_type == "wait":
                success = self.robot.wait(params.get("reason"))
                return {"success": success, "action": "wait"}
            
            else:
                return {"success": False, "action": action_type, "error": "Unknown action"}
        
        except Exception as e:
            return {"success": False, "action": action_type, "error": str(e)}


